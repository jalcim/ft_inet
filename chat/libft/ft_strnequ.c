#include "libft.h"

int			ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		i;

	i = 0;
	while (n && s1[i] == s2[i] && (s1[i] && s2[i]))
	{
		i++;
		n--;
	}
	if ((!s1[i] && !s2[i]) || !n)
		return (1);
	return (0);
}
